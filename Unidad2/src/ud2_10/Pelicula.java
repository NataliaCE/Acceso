package ud2_10;

public class Pelicula {
	private int id;
	private String titulo;
	private int anyo;
	private String descripcion;
	
	public Pelicula(int id, String titulo, int anyo, String descripcion) {
		super();
		this.id = id;
		this.titulo = titulo;
		this.anyo = anyo;
		this.descripcion = descripcion;
	}
	
}
